package day4_variables;

public class FloatingNumbers {
    public static void main(String[] args) {
         /*
Primitive Data Types:
-Whole Numbers/Integer Type: byte, short, int, long ----> if we use data directly Java takes it as int by defolt
-Decimal number/Floating Type: float, double ----> if the data used directly Java takes it "double" by the defolt
 */
// to be able to store fractions or decimals we use FLOATING/DECIMAL numbers
        //we have DOUBLE and FLOAT
        double price = 3.99;

        float average = 0.29f; //for the float like for long we put L in the end we put F in the end for float
        System.out.println(price);
        System.out.println(average);
        System.out.println(0.19);
    }
}