package day07_relational_operators;

public class OperatorPrictice {
    public static void main(String[] args) {
        int b = 12;
        b++; // post increment
        System.out.println(b); //13

        ++b; //PRE increment
        System.out.println(b);

        b--; // POST decrement
        System.out.println(b);

        --b; //PRE decrement
        System.out.println(b);

    }
}
