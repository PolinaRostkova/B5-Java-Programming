package day08_scanner_logical_operators;

public class PrintName {
    public static void main(String[] args) {

        String name = "Tom Jerry"; // we assigned data in hard coded way
        System.out.println("Hello, "+name); // Using variable name dynamically
        System.out.println("Hello , Tom Jerry");  // Using "Tom Jerry" statically / hard coded way

        System.out.println("WHat is your name: ");
        System.out.println("My name is: "+name);
    }
}
